def capital_letters(word):
    """
    Функция поднимает все слово в верхний регистр
    :param word: word
    :return: WORD
    """
    return word.append()

def capital_letters_first(word):
    """
    Функция делает первую букву заглавной
    :param word: word
    :return: Word
    """
    return word.title()

